def main():
    out = []
    problems = []
    for i, item in enumerate(Q, 1):
        (q, d, t, a, path, title, ev, tol, judge, anchor, lo, hi, override) = item
        hits = find_pages(anchor, lo, hi)
        if not hits:
            problems.append((i, q, f"anchor not found near {lo}-{hi}: {anchor[:30]}"))
            gold = override if override else f"{lo}-{hi}"
        else:
            # primary provision text appears at the first hit (before 해설/참고자료 repeats)
            gold = str(hits[0])
            if override and override != gold:
                # keep override only if it is itself a real hit; otherwise trust first hit
                opages = [int(x) for x in (override.split('-') if '-' in override else [override])]
                if all(p in hits for p in opages):
                    gold = override
        obj = {
            "id": f"gmp_eval_{i:03d}",
            "question": q,
            "difficulty": d,
            "question_type": t,
            "expected_answer": a,
            "gold_pages": gold,
            "gold_section_title": title,
            "gold_section_path": path,
            "gold_evidence_summary": ev,
            "acceptable_page_tolerance": tol,
            "retrieval_target": {
                "must_hit_section": True,
                "must_hit_page_range": True,
                "minimum_required_pages": gold,
            },
            "answer_judging_notes": judge,
        }
        out.append(obj)

    with open('eval/gmp_eval_testset.jsonl', 'w', encoding='utf-8') as f:
        for o in out:
            f.write(json.dumps(o, ensure_ascii=False) + "\n")

    # stats
    from collections import Counter
    dc = Counter(o['difficulty'] for o in out)
    tc = Counter(o['question_type'] for o in out)
    print("TOTAL:", len(out))
    print("difficulty:", dict(dc))
    print("type:", dict(tc))
    print("PROBLEMS:", len(problems))
    for p in problems:
        print("  ", p)


if __name__ == '__main__':
    main()
